package tiposDeDados;

public class binarios {
	
	public static void main(String[] args) {
		
		// Literais binarios
		
		// byte, -127 a 127
		byte var1 = 0b110;    // binario de 6
		byte var2 = 6;
		
		System.out.println(var1 + " é igual a " + var2);
		
		short var3 = 0b110;
		int var4 = 0b110;
		long var5 = 0b110;
		
		System.out.println(var3 + " é igual a " + var4 + " e a " + var5);
		
		
	}

}
